package Entidades;

public class ProdutoEletronico {
	public String nome;
	public String modelo;
	public double preco;
	
	public ProdutoEletronico(){
		
	}

	public ProdutoEletronico(String nome, String modelo, double preco) {
		super();
		this.nome = nome;
		this.modelo = modelo;
		this.preco = preco;
	}

}
