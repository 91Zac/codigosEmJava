package Entidades;

public class Funcionario {
	public String nome;
	public String cargo;
	public double salario;
	public String matricula;
	
	public Funcionario() {
	}
	
	public Funcionario(String nome, String cargo, double salario, String matricula) {
		this.nome = nome;
		this.cargo = cargo;
		this.salario = salario;
		this.matricula = matricula;
	}
	
}
