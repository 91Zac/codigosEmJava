package Entidades;

public class Aviao {
	public String modelo;
	public String tamanho;
	public int numeroViagensRealizadas;
	
	public Aviao(){
		
	}

	public Aviao(String modelo, String tamanho, int numeroViagensRealizadas) {
		this.modelo = modelo;
		this.tamanho = tamanho;
		this.numeroViagensRealizadas = numeroViagensRealizadas;
	}
	
}


